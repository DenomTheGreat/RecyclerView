package com.example.naveen.recyclerview.Utility;

public class ListItem {

    private String title;
    private String desc;

    public ListItem(String title, String desc) {
        this.title = title;
        this.desc = desc;
    }

    public String getTitle() {
        return title;
    }

    public String getDesc() {
        return desc;
    }
}
